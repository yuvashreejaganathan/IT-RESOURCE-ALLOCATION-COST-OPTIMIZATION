    EmployeeID INT,
    ProjectID INT,
    HoursWorked INT,
    RoleInProject VARCHAR(50),
    FOREIGN KEY (EmployeeID) REFERENCES Employees(EmployeeID),
    FOREIGN KEY (ProjectID) REFERENCES Projects(ProjectID)
);

